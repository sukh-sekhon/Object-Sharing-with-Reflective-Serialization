/**
 * Sample primitive object containing 3 ints
 *
 * @author Sukhjot Sekhon
 */
public class ObjPrimitive {
    private int firstInt;
    private int secondInt;
    private int thirdInt;

    public ObjPrimitive() {}

    public ObjPrimitive(int inFirst, int inSecond, int inThird) {
        firstInt = inFirst;
        secondInt = inSecond;
        thirdInt = inThird;
    }
}
